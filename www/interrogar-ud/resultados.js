function tudo(event) {
    var checkboxes, i, anotacoes;
    checkboxes = document.getElementsByClassName("cb");
    anotacoes = document.getElementsByClassName("anotacao");

    if (event == "marcar"){
        for (i = 0; i < checkboxes.length; i++) {
            checkboxes[i].checked = true;
        }
    }

    if (event == "desmarcar"){
        for (i = 0; i < checkboxes.length; i++) {
            checkboxes[i].checked = false;
        }
    }

    if (event == "abrir") {
        for (i = 0; i < anotacoes.length; i++) {
            if (Object.values(translations['Mostrar anotação']).indexOf(anotacoes[i].innerHTML) > -1) {
                anotacoes[i].click();
            }
        }
    }

    if (event == "fechar") {
        for (i = 0; i < anotacoes.length; i++) {
            if (Object.values(translations['Esconder anotação']).indexOf(anotacoes[i].innerHTML) > -1) {
                anotacoes[i].click();
            }
        }
    }

}

